package com.example.foundemotions.dtos;

public class ArtistasPorGeneroDTO {
    private String name;
    private String artistaname;
    private String generoname;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getArtistaname() {
        return artistaname;
    }

    public void setArtistaname(String artistaname) {
        this.artistaname = artistaname;
    }

    public String getGeneroname() {
        return generoname;
    }

    public void setGeneroname(String generoname) {
        this.generoname = generoname;
    }
}
